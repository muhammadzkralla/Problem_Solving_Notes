struct node {
    int x;
    int y;
    bool operator<(const node& other) const {
        return (x < other.x) || (x == other.x && y < other.y);
    }
};

void solve() {
    ll n, k, ans = -1;
    cin >> n >> k;
    ll l = 0, r = k, all = (k * (k - 1) / 2) + 1;
    while (l <= r) {
        ll mid = l + (r - l) / 2;
        ll diff = (mid * (mid - 1) / 2);

        if (all - diff >= n) {
            ans = k - mid;
            l = mid + 1;
        } else r = mid - 1;
    }

    cout << ans << endl;

}

int main() {
    fast();
    int t = 1;
    while (t--){
        solve();
    }
}