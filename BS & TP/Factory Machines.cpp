auto mod = 1e9 + 7;

void fast() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
}

struct node {
    int x;
    int y;
    bool operator<(const node& other) const {
        return (x < other.x) || (x == other.x && y < other.y);
    }
};

void solve() {
    ll n, t;
    cin >> n >> t;
    vector<ll> v(n);
    for (int i = 0; i < n; ++i) cin >> v[i];

    ll l = 1, r = 1e18, ans = r;

    while (l <= r) {
        ll mid = l + (r - l) / 2, tmp = 0;
        for (int i = 0; i < n; ++i) {
            tmp += mid / v[i];
            if (tmp >= t) {
                ans = mid;
                break;
            }
        }

        if (ans == mid) r = mid - 1;
        else l = mid + 1;
    }

    cout << ans << endl;
}

int main() {
    fast();
    int t = 1;
    while (t--){
        solve();
    }
}